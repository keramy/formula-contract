export async function getArchitectureFirms(): Promise<ActionResult<CrmFirmWithLinks[]>> {
  try {
    const { error, supabase } = await requireCrmAccess();
    if (error) return { success: false, error };

    const { data, error: dbError } = await supabase!
      .from("crm_architecture_firms")
      .select(`
        *,
        crm_brand_firm_links(
          relationship_type,
          brand:crm_brands(id, name, brand_code)
        )
      `)
      .eq("is_deleted", false)
      .order("priority", { ascending: true })
      .order("name", { ascending: true });

    if (dbError) return { success: false, error: dbError.message };

    // Transform the joined data to match CrmFirmWithLinks shape
    // eslint-disable-next-line @typescript-eslint/no-explicit-any
    const firms = (data || []).map((f: any) => ({
      ...f,
      brand_links: (f.crm_brand_firm_links || []).map(
        // eslint-disable-next-line @typescript-eslint/no-explicit-any
        (l: any) => ({
          brand: l.brand as { id: string; name: string; brand_code: string },
          relationship_type: l.relationship_type as string | null,
        })
      ),
      crm_brand_firm_links: undefined,
    })) as CrmFirmWithLinks[];

    return { success: true, data: firms };
  } catch (err) {
    console.error("getArchitectureFirms error:", err);
    return { success: false, error: "Failed to fetch firms" };
  }
}

