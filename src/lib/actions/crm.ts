export async function getCrmDashboardStats(): Promise<ActionResult<CrmDashboardStats>> {
  try {
    const { error, supabase } = await requireCrmAccess();
    if (error) return { success: false, error };

    // Parallel queries for dashboard stats
    const [brandsResult, firmsResult, oppsResult, actionsResult] = await Promise.all([
      supabase!.from("crm_brands").select("tier", { count: "exact" }).eq("is_deleted", false),
      supabase!.from("crm_architecture_firms").select("vendor_list_status", { count: "exact" }).eq("is_deleted", false),
      supabase!.from("crm_opportunities").select("stage, estimated_value, currency").eq("is_deleted", false),
      supabase!.from("crm_activities").select("next_action_date", { count: "exact" })
        .eq("is_deleted", false)
        .not("next_action", "is", null)
        .gte("next_action_date", new Date().toISOString().split("T")[0]),
    ]);

    const brands = brandsResult.data || [];
    const firms = firmsResult.data || [];
    const opps = oppsResult.data || [];

    // Calculate brand tier breakdown
    const brandsByTier = { luxury: 0, mid_luxury: 0, bridge: 0 };
    for (const b of brands) {
      if (b.tier in brandsByTier) brandsByTier[b.tier as keyof typeof brandsByTier]++;
    }

    // Vendor approved count
    const vendorApproved = firms.filter((f) => f.vendor_list_status === "approved").length;

    // Active opportunities (not won/lost)
    const activeOpps = opps.filter((o) => o.stage !== "won" && o.stage !== "lost");
    const totalPipelineValue = activeOpps.reduce(
      (sum, o) => sum + (Number(o.estimated_value) || 0),
      0
    );

    // Opportunities by stage
    const opportunitiesByStage: Record<OpportunityStage, number> = {
      researched: 0, contacted: 0, sample_sent: 0, meeting: 0,
      proposal: 0, negotiation: 0, won: 0, lost: 0,
    };
    for (const o of opps) {
      if (o.stage in opportunitiesByStage) opportunitiesByStage[o.stage as OpportunityStage]++;
    }

    return {
      success: true,
      data: {
        brandCount: brands.length,
        brandsByTier,
        firmCount: firms.length,
        vendorApproved,
        activeOpportunities: activeOpps.length,
        totalPipelineValue,
        pipelineCurrency: "USD",
        upcomingActions: actionsResult.count || 0,
        opportunitiesByStage,
      },
    };
  } catch (err) {
    console.error("getCrmDashboardStats error:", err);
    return { success: false, error: "Failed to fetch dashboard stats" };
  }
}
