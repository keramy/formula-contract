export interface ActionResult<T = void> {
  success: boolean;
  data?: T;
  error?: string;
}

// ============================================================================
// Auth Helpers
// ============================================================================

async function requireCrmAccess(requiredRoles: string[] = ["admin", "management"]) {
  const supabase = await createClient();
  const { data: { user } } = await supabase.auth.getUser();

  if (!user) {
    return { error: "Not authenticated" as const, supabase: null, user: null, role: null };
  }

  const role = await getUserRoleFromJWT(user, supabase);
  if (!requiredRoles.includes(role)) {
    return { error: "Not authorized" as const, supabase: null, user: null, role: null };
  }

  return { error: null, supabase, user, role };
}

